<?php

    $num1 = 10;
    $num2 = 2;

    // ADDITION
    echo $num1 + $num2;

    // SUBTRACTION
    echo $num1 - $num2;

    // MULTIPLY
    echo $num1 * $num2;

    // DIVISION
    echo $num1 / $num2;

    // MODULUS
    echo $num1 % $num2;

    // EXPONENTIATION
    echo $num1 ** $num2;

    // POST-INCREMENT/DECREMENT
    $num1++;

    // PRE-INCREMENT/DECREMENT
    ++$num1;



?>